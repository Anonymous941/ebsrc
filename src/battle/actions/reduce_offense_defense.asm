
BTLACT_REDUCEOFFDEF: ;$C28F21
	REP #PROC_FLAGS::ACCUM8 | PROC_FLAGS::INDEX8 | PROC_FLAGS::CARRY
	RESERVE_STACK_SPACE_CLOBBER 24
	JSR a:.LOWORD(ATK_FAIL_CHK)
	CMP #$0000
	BNE @UNKNOWN0
	LDX .LOWORD(CURRENT_TARGET)
	LDY a:battler::offense,X
	STY $16
	LDA .LOWORD(CURRENT_TARGET)
	JSR a:.LOWORD(HEXADECIMATE_OFFENSE)
	LOADPTR TEXT_BATTLE_OFFENSE_WENT_DOWN, $0E
	LDX .LOWORD(CURRENT_TARGET)
	LDY $16
	TYA
	SEC
	SBC a:battler::offense,X
	STA $06
	STZ $08
	MOVE_INT $06, $12
	JSL DISPLAY_TEXT_WAIT
	LDX .LOWORD(CURRENT_TARGET)
	LDY a:battler::defense,X
	STY $16
	LDA .LOWORD(CURRENT_TARGET)
	JSR a:.LOWORD(HEXADECIMATE_DEFENSE)
	LOADPTR TEXT_BATTLE_DEFENSE_WENT_DOWN, $0E
	LDX .LOWORD(CURRENT_TARGET)
	LDY $16
	TYA
	SEC
	SBC a:battler::defense,X
	STA $06
	STZ $08
	MOVE_INT $06, $12
	JSL DISPLAY_TEXT_WAIT
@UNKNOWN0:
	PLD
	RTL
