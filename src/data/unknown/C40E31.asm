; Format:
; 00XX YYYY
; Valid XX:
;   01 = Show frame described at $YYYY ($0000 for nothing)
;   02 = Wait YYYY frames
;   03 = Jump to $YYYY
; Sweating script
SPRITE_OVERLAY_SWEATING:
	SHOWFRAME .LOWORD(SPRITE_OVERLAY_SWEATING_FRAME_1)
	DELAYNEXTFRAME $0008
	SHOWFRAME .LOWORD(SPRITE_OVERLAY_SWEATING_FRAME_2)
	DELAYNEXTFRAME $0008
	SHOWFRAME $0000
	DELAYNEXTFRAME $0010
	SHOWFRAME .LOWORD(SPRITE_OVERLAY_SWEATING_FRAME_3)
	DELAYNEXTFRAME $0008
	SHOWFRAME .LOWORD(SPRITE_OVERLAY_SWEATING_FRAME_4)
	DELAYNEXTFRAME $0008
	SHOWFRAME $0000
	DELAYNEXTFRAME $0010
	JUMPTO .LOWORD(SPRITE_OVERLAY_SWEATING)

; Mushroomized script
