
FILL_CHARACTER_PP_TILE_BUFFER: ;$C20F26
	REP #PROC_FLAGS::ACCUM8 | PROC_FLAGS::INDEX8 | PROC_FLAGS::CARRY
	PHD
	PHA
	TDC
	ADC #$FFF0
	TCD
	PLA
	STY $04
	STA $02
	LDY $1E
	STY $0E
	LDA a:.LOWORD(RAM)+STATUS_GROUP::CONCENTRATION,X
	AND #$00FF
	BEQ @CAN_CONCENTRATE
	LDA $02
	JSR a:.LOWORD(FILL_HP_PP_TILE_BUFFER_X)
	BRA @RETURN
@CAN_CONCENTRATE:
	LDA $04
	JSR a:.LOWORD(SEPARATE_DECIMAL_DIGITS)
	LDY $0E
	LDX #$0001
	LDA $02
	JSR a:.LOWORD(FILL_HP_PP_TILE_BUFFER)
@RETURN:
	PLD
	RTS
